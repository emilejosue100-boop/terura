import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const frontendRoot = path.resolve(__dirname, '..');
const backendUrl = (process.env.BACKEND_URL || process.env.VITE_API_URL || '').trim().replace(/\/$/, '');

const vercelConfigPath = path.join(frontendRoot, 'vercel.json');
const config = JSON.parse(fs.readFileSync(vercelConfigPath, 'utf8'));

const rewrites = [];

if (backendUrl) {
  rewrites.push({
    source: '/api/:path*',
    destination: `${backendUrl}/api/:path*`,
  });
  console.log(`[vercel] API proxy enabled: /api/* -> ${backendUrl}/api/*`);
} else {
  console.warn(
    '[vercel] BACKEND_URL is not set. Add it in Vercel Environment Variables (your Render URL, e.g. https://terura-api.onrender.com).'
  );
}

rewrites.push({
  source: '/(.*)',
  destination: '/index.html',
});

config.rewrites = rewrites;
fs.writeFileSync(vercelConfigPath, `${JSON.stringify(config, null, 2)}\n`);

// Flag for client build so we skip the misconfiguration banner when proxy is configured.
const proxyFlagPath = path.join(frontendRoot, 'src', 'generated');
fs.mkdirSync(proxyFlagPath, { recursive: true });
fs.writeFileSync(
  path.join(proxyFlagPath, 'apiProxy.ts'),
  `// Auto-generated by scripts/generate-vercel-config.mjs — do not edit\nexport const HAS_API_PROXY = ${backendUrl ? 'true' : 'false'};\n`
);
